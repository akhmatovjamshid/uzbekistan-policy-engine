/**
 * Apply translations to the current model page.
 * Looks for [data-i18n] attributes and sets textContent.
 */
window.applyModelLang = function(lang) {
  const t = window.MODEL_LANGS[lang];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });
  // Swap tooltip text for .ptip elements that carry data-tip-{lang} attributes
  const tipAttr = 'data-tip-' + lang;
  document.querySelectorAll('.ptip[' + tipAttr + ']').forEach(el => {
    const tip = el.getAttribute(tipAttr);
    if (tip) el.setAttribute('data-tip', tip);
  });
  // Store current lang for newly rendered dynamic content
  document.documentElement.setAttribute('lang', lang);
};

/**
 * Listen for language change message from parent hub.
 */
window.addEventListener('message', function(e) {
  if (e.data && e.data.type === 'setLang') {
    window.applyModelLang(e.data.lang);
    // Re-render QPM charts (irfTitle, KPI "at quarter", chart legends) if available
    if (typeof run === 'function' && typeof lastIRF !== 'undefined' && lastIRF) {
      try { run(); } catch(_) {}
    }
  }
});
